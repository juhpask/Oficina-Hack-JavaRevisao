package aula17;

public class exer02 {
    
    public static void main(String[] args) {
        /*
        Entrada:
            A primeira linha contém um integer
            A segunda linha contém um double.
            A terceira linha contém uma String.
        Saída:
            Na primeira linha imprima String: seguida da String lida sem alterações.
            Na primeira linha imprima Double: seguida do double lido sem alterações.
            Na primeira linha imprima Int: seguida do Integer lido sem alterações.
        */
        
        Integer num1 = 42;
        Double num2 = 3.1415;
        String exemplo = "Este é o exercício de entrada e saída padrão em Java!";
        
        System.out.println("String: " + exemplo);
        System.out.println("Double: " + num2);
        System.out.println("Int: " + num1);

    }
    
}